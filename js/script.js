const menu_btn_02 = document.querySelector('.menu_btn_02');
const bar_02 = menu_btn_02.children;
menu_btn_02.addEventListener('click',()=>{
    const bar1_02 = bar_02[0];
    const bar2_02 = bar_02[1];
    const bar3_02 = bar_02[2];

    bar1_02.classList.toggle('bar1_02');
    bar2_02.classList.toggle('bar2_02');
    bar3_02.classList.toggle('bar3_02');
})
// Button 02 End


